function averageOfFour(num1, num2, num3, num4) {
    // Calculate the average of the four numbers
    return (num1 + num2 + num3 + num4) / 4;
  }


//   This function adds up the four input numbers and then divides the sum by 4 to calculate the average. The provided test cases demonstrate the usage of the averageOfFour function.
  
  // Test cases
  console.log(averageOfFour(10, 4, 12, 3));    // 7.25
  console.log(averageOfFour(-20, 50, 4, 21));   // 13.75
  console.log(averageOfFour(10, 4, 12, 3));    // 7.25
  console.log(averageOfFour(5, 5, 3, 7));      // 5
  