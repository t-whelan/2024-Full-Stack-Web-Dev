function larger(num1, num2) {
    // Return the larger of the two numbers
    return Math.max(num1, num2);
  }

//   This function uses the Math.max function to find the maximum of the two input numbers. The provided test cases demonstrate the usage of the larger function.
  
  // Test cases
  console.log(larger(256, 400));    // 400
  console.log(larger(31, 4));       // 31
